package section2.heap;

public class Pair4 {
	int i,x,y, d;

	public Pair4(int i, int x, int y, int d) {
		super();
		this.i = i;
		this.x = x;
		this.y = y;
		this.d = d;
	}

	public int getI() {
		return i;
	}

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}

	public int getD() {
		return d;
	}

	@Override
	public String toString() {
		return "Pair4 [i=" + i + ", x=" + x + ", y=" + y + ", d=" + d + "]\n";
	}

}
